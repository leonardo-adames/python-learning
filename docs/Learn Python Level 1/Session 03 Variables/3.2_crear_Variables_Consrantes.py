# Comenzamos creando las variables
food = "Macarrones"    # (food) es la variable, ("Macarrones") es el valor asignado
number_value = 123     # (number_value) es la variable, (123) es el valor asignado

print(food)            # Salida: Macarrones
print(number_value)    # Salida: 123

# Reasignar los valores existentes

food = True              # Ahora (food) tiene un nuevo valor (True)
number_value = "123"     # Ahora (number_value) tiene un nuevo valor ("123")

print(food)             # Salida: True 
print(number_value)     # Salida: 123