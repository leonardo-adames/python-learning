# ACCESO A ELEMENTO POR ÍNDICE

# Lista de ejemplo
frutas = ["manzana", "banana", "cereza", "mango"]
print("\n")

# Acceso por índice positivo
print("Primera fruta:", frutas[0])   # manzana
print("Tercera fruta:", frutas[2])   # cereza
print("\n")

# Acceso por índice negativo (desde el final)
print("Última fruta:", frutas[-1])   # mango
print("Penúltima fruta:", frutas[-2]) # cereza
print("\n")



